# მოიყვანეთ რეალურ ცხოვრებაში ფუნქციის რამოდენიმე მაგალითი
# 1. ტელეფონის ზარის ფუნქცია - როდესაც ვიღებთ ზარს, ტელეფონი რეკავს და ჩვენ შეგვიძლია პასუხის გაცემა ან უარის თქმა.
# 2. მანქანის სტარტის ფუნქცია - როდესაც ვრთავთ მანქანას, ის იწყებს მუშაობას და მზად არის მოძრაობისთვის.
# 3. კომპიუტერის გამორთვის ფუნქცია - როდესაც ვრთავთ კომპიუტერს, ის იწყებს მუშაობას და როდესაც ვრთავთ მას, ის გამორთულია.