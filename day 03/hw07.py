#შექმენით 2 ცვლადი:
#პირველ ცვლადში შეინახეთ უარყოფითი int
#მეორე ცვლადში შეინახეთ უარყოფითი float
#მოახდინეთ ამ ცვლადებზე: მიმატება, გამოკლება, გაყოფა, გამრავლება, ნაშთით გაყოფა, ახარისხება

#ცვლადების შექმნა
num1 = -8
num2 = -2.5

#ოპერაციები

#მიმატება
print(num1+num2)

#გამოკლება
print(num1-num2)

#გამრავლება
print(num1*num2)

#გაყოფა
print(num1/num2)

#უნაშთოდ გაყოფა
print(num1//num2)

#ახარისხება
print(num1**num2)